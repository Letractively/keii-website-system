<p class="B_text_B">
	<strong>Emissivity [e] </strong>
</p>
<p>The ratio of a target surface’s radiance to that of a blackbody at
	the same temperature, viewed from the same angle and over the same
	spectral interval; a generic look-up value for a material. Values range
	from 0 to 1.0. Alternatively, the ratio of a flat, optically polished,
	opaque target surface radiance to that of a blackbody at the same
	temperature, viewed from the same angle and over the same spectral
	interval.</p>


<p class="B_text_B">
	<strong>Emittance [e]</strong>
</p>
<p>The ratio of a target surface’s radiance to that of a blackbody at
	the same temperature, viewed from the same angle over the same spectral
	interval; a generic look-up value for a material. Values range from 0
	to 1.0.</p>


<p class="B_text_B">
	<strong>Fahrenheit </strong>
</p>
<p>A temperature scale based on 32°F as the freezing point of water and
	212°F as the boiling point of water at standard atmospheric pressure; a
	relative scale related to the Rankine scale [0°F = 459.67.R; 1 F° (DT)
	= 1 R (DT)].</p>


<p class="B_text_B">
	<strong>Field of View [FOV] </strong>
</p>
<p>The angular subtense (expressed in angular degrees or radians per
	side if rectangular, and angular degrees or radians if circular) over
	which an instrument will integrate all incoming radiant energy. In a
	radiation thermometer this defines the target spot size; in a scanner
	or imager this defines the scan angle or picture size or total field of
	view (TFOV).</p>
<p class="B_text_B">
	<strong>Focal Plane Array [FPA]</strong>
</p>
<p>A linear or two-dimensional matrix of detector elements, typically
	used at the focal plane of an instrument. In thermography, rectangular
	FPAs are used in “staring” (non-scanning) infrared imagers. These are
	called IRFPA imagers.</p>

<p class="B_text_B">
	<strong>Graybody</strong>
</p>
<p>A radiating object whose emissivity is a constant value less than
	unity (1.0). over a specific spectral range.</p>

<p class="B_text_B">
	<strong>Hertz [Hz]</strong>
</p>
<p>A unit of measurement of signal frequency; 1 Hz = cycle per second.</p>

<p class="B_text_B">
	<strong>Imager, Infrared</strong>
</p>
<p>An infrared instrument that collects the infrared radiant energy from
	a target surface and produces an image in monochrome (black and white)
	or color, where the gray shades or color hues correspond respectively
	to target exitance.</p>


<p class="B_text_B">
	<strong>Infrared [IR]</strong>
</p>
<p>The infrared spectrum is loosely defined as that portion of the
	electromagnetic continuum extending from the red visible (0.75 mm to
	about 1,000 mm) . Because of instrument design considerations and the
	infrared transmission characteristics of the atmosphere, however, most
	infrared measurements are made between 0.75 and 20 mm.</p>
<p class="B_text_B">
	<strong>Infrared Focal Plane Array [IRFPA]</strong>
</p>
<p>A linear or two-dimensional matrix of individual infrared detector
	elements, typically used as a detector in an infrared imaging
	instrument.</p>


<p class="B_text_B">
	<strong>Infrared Thermal Imager</strong>
</p>
<p>An Instrument or system that converts incoming infrared radiant
	energy from a target surface to a thermal map, or thermogram, on which
	color hues or gray shades can be related to the temperature
	distribution on that surface.</p>

<p class="B_text_B">
	<strong>Instantaneous Field of View [IFOV]</strong>
</p>
<p>The angular subtense (expressed in angular degrees or radians per
	side if rectangular and angular degrees or radians if round) found by
	the ratio of the detector dimension divided by the instrument focal
	length; the projection of the detector at the target plane.</p>


<p class="B_text_B">
	<strong>Isotherm</strong>
</p>
<p>A pattern superimposed on a thermogram or on a line scan that
	includes or highlights all points that have the same apparent
	temperature.</p>
