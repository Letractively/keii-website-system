<p class="B_text_B">
	<strong>Delta T</strong>
</p>
<p>the temperature difference between two targets usually of comparable
	targets under comparable conditions.</p>

<p class="B_text_B">
	<strong>Detector, Infrared</strong>
</p>
<p>A transducer element that converts incoming infrared radiant energy
	impinging on its sensitive surface into a useful electrical signal.</p>

<p class="B_text_B">
	<strong>Effective Emissivity [e]</strong> <br> (also called emittance,
	but emittance is a less preferable term because it was formerly used to
	describe radiant exitance)
</p>
<p>The measured emissive value of a particular surface under existing
	measurement conditions (rather than the generic tabulated value for the
	surface material) that can be used to correct a specific measuring
	instrument to provide a correct temperature measurement.</p>


