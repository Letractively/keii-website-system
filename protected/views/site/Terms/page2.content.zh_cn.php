

<p class="B_text_B">
	<strong>术语解释</strong>
</p>

<p class="B_text_B">
	<strong>Detector探测器</strong>
</p>
<p>红外热像仪的传感器，探测到物体的红外辐射能并将其转换为电信号。探测器的最小单元为像素。</p>

<p class="B_text_B">
	<strong>Dewpoint露点</strong>
</p>
<p>露点是露点温度的简称，露点温度是指在大气压力不变的情况下，由于冷却作用，空气里原来所含的未饱和水蒸汽变成100%饱和时的温度，称为露点温度。</p>

<p class="B_text_B">
	<strong>Emissivity 发射率 (ε):</strong>
</p>
<p>物体散发红外辐射的能力。ε值与被测物体的材质属性，被测对象表面特性以及被测物体的温度有关</p>

<p class="B_text_B">
	<strong>Fahrenheit华氏温度</strong>
</p>
<p>是在北美使用比较广泛的一种温标。℉ = (℃ x 1.8) + 32。</p>

<p class="B_text_B">
	<strong>Field of view 视场角(FOV)</strong>
</p>
<p>FOV是指物体在热像仪中完整成像的水平角度和垂直角度。</p>

<p class="B_text_B">
	<strong>Focal Plane array焦平面红外探测器(FPA)</strong>
</p>
<p>FPA探测器早期为制冷型探测器，并且体积较大，用于近红外波段测量；现在的FPA探测器已发展为非制冷型，用于远红外波段进行高精度测量。探测器接收到物体辐射能后导致传感器温度升高从而改变传感器的阻值，并以电信号将其阻值改变表现出来。</p>

<p class="B_text_B">
	<strong>Grey body radiator (real body)灰色辐射物</strong>
</p>
<p>自然界的绝大多数物体都是
	“灰色辐射体”。与黑体不同的是，灰体无法吸收所有的入射光波，通常反射或传导都会同时存在。灰色辐射体的发射率均小于1。</p>

<p class="B_text_B">
	<strong>Thermal Imager 红外热像仪</strong>
</p>
<p>红外热像仪是指能够检测电磁波光谱在红外波段的辐射，并能将不可见的红外辐射变成可视图片的检测设备。热像仪目前最主要的功能是测温和成像。</p>

<p class="B_text_B">
	<strong>Infrared radiation 红外辐射</strong>
</p>
<p>是电磁辐射的一种。只要是绝对零度以上的物体均发射红外辐射。</p>

<p class="B_text_B">
	<strong>Isotherms等温线</strong>
</p>
<p>
可设置温度范围，并用相同的颜色将在此范围内的所有相同温度点标注出来。
</p>

<p class="B_text_B">
	<strong>Kelvin（开尔文）</strong>
</p>
<p>国际通用的温标之一。0 K 相当于绝对零度zero (-273.15°C)。 请见以下转换公式：273.15 K = 0℃= 32℉。K
	= ℃+ 273.15。</p>

<p class="B_text_B">
	<strong>Kirchhoff’s radiation law 基尔霍夫辐射定律</strong>
</p>
<p>是著名的热学定律，描述了一定波长的物体发射率与吸收比之间的关系：在热平衡条件下，物体对热辐射的吸收比恒等于同温度下的发射率。</p>

